package com.kfwy.hkp.crm.customer.dto;

import com.kfwy.hkp.sys.user.entity.UserEntity;

import java.util.Date;
import java.util.List;


/**
 * 集中获客记录dto
 */
public class FocusCusDto extends CustomerFocusCusDto {

    protected Date focusTime;

    protected Date seeCusTime;

    protected String userCode;

    protected String userName;

    protected Integer followCount;

    public Integer getFollowCount () {
        return followCount;
    }

    public void setFollowCount (Integer followCount) {
        this.followCount = followCount;
    }

    public Date getSeeCusTime () {
        return seeCusTime;
    }

    public void setSeeCusTime (Date seeCusTime) {
        this.seeCusTime = seeCusTime;
    }
    public String getUserCode () {
        return userCode;
    }

    public void setUserCode (String userCode) {
        this.userCode = userCode;
    }

    public String getUserName () {
        return userName;
    }

    public void setUserName (String userName) {
        this.userName = userName;
    }

    protected List<UserEntity> userList;


    public Date getFocusTime() {
        return focusTime;
    }

    public void setFocusTime(Date focusTime) {
        this.focusTime = focusTime;
    }

    public List<UserEntity> getUserList() {
        return userList;
    }

    public void setUserList(List<UserEntity> userList) {
        this.userList = userList;
    }

}
