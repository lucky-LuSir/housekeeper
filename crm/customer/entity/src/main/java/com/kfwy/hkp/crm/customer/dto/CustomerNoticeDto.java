package com.kfwy.hkp.crm.customer.dto;


import com.gniuu.framework.service.AbstractDto;
import com.kfwy.hkp.crm.customer.entity.CustomerEntity;

/**
 * Created by jupe on 2018/5/8.
 */
public class CustomerNoticeDto extends AbstractDto {

    private CustomerEntity customerEntity;

    private String type;

    private String createCode;

    private String updateCode;

    public CustomerEntity getCustomerEntity() {
        return customerEntity;
    }

    public void setCustomerEntity(CustomerEntity customerEntity) {
        this.customerEntity = customerEntity;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCreateCode() {
        return createCode;
    }

    public void setCreateCode(String createCode) {
        this.createCode = createCode;
    }

    public String getUpdateCode() {
        return updateCode;
    }

    public void setUpdateCode(String updateCode) {
        this.updateCode = updateCode;
    }

}
