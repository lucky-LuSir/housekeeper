package com.kfwy.hkp.crm.houseowner.entity;

import com.gniuu.framework.entity.BaseEntity;

/**
 * Create By hjh on 2018/10/18
 */
public class HouseownerFollowupEntity extends BaseEntity {

    protected String houseownerCode;

    // 1 电话拜访 2 上门拜访

    protected String followupType;

    protected String followupContext;

    protected String empPhone;

    protected String ownerPhone;

    

    public String getHouseownerCode() {
        return houseownerCode;
    }

    public void setHouseownerCode(String houseownerCode) {
        this.houseownerCode = houseownerCode;
    }

    public String getFollowupType() {
        return followupType;
    }

    public void setFollowupType(String followupType) {
        this.followupType = followupType;
    }

    public String getFollowupContext() {
        return followupContext;
    }

    public void setFollowupContext(String followupContext) {

        this.followupContext = followupContext;
    }

    public String getEmpPhone() {
        return empPhone;
    }

    public void setEmpPhone(String empPhone) {
        this.empPhone = empPhone;
    }

    public String getOwnerPhone() {
        return ownerPhone;
    }

    public void setOwnerPhone(String ownerPhone) {
        this.ownerPhone = ownerPhone;
    }
}
