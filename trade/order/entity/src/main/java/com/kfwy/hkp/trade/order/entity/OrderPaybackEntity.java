package com.kfwy.hkp.trade.order.entity;

import com.gniuu.framework.entity.BaseEntity;

import java.math.BigDecimal;
import java.util.Date;

/**
 * @Author:ChenJie Created by kfwy_it_009 on 2018/11/9.
 */
public class OrderPaybackEntity  extends BaseEntity {
    //订单编码
    private String orderCode;
    private String payBackOrderCode;
    //回款金额
    private BigDecimal payment;
    //回款时间
    private Date backTime;
    //订单时间
    private Date orderTime;
    //返佣扣款
    private BigDecimal commissionDeductions;
    //税金扣款
    private BigDecimal taxDeductions;
    /**
     * 汇款描述
     */
    private String description;
    /**
     * 付款渠道支付宝、银行、微信
     */
    private String channel;

    private String fileCode;
    public String getPayBackOrderCode() {
        return payBackOrderCode;
    }

    public void setPayBackOrderCode(String payBackOrderCode) {
        this.payBackOrderCode = payBackOrderCode;
    }

    public String getOrderCode() {
        return orderCode;
    }

    public void setOrderCode(String orderCode) {
        this.orderCode = orderCode;
    }

    public BigDecimal getPayment() {
        return payment;
    }

    public void setPayment(BigDecimal payment) {
        this.payment = payment;
    }

    public Date getBackTime() {
        return backTime;
    }

    public void setBackTime(Date backTime) {
        this.backTime = backTime;
    }

    public Date getOrderTime() {
        return orderTime;
    }

    public void setOrderTime(Date orderTime) {
        this.orderTime = orderTime;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getChannel() {
        return channel;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }

    public String getFileCode() {
        return fileCode;
    }

    public void setFileCode(String fileCode) {
        this.fileCode = fileCode;
    }

    public BigDecimal getCommissionDeductions() {
        return commissionDeductions;
    }

    public void setCommissionDeductions(BigDecimal commissionDeductions) {
        this.commissionDeductions = commissionDeductions;
    }

    public BigDecimal getTaxDeductions() {
        return taxDeductions;
    }

    public void setTaxDeductions(BigDecimal taxDeductions) {
        this.taxDeductions = taxDeductions;
    }
}
