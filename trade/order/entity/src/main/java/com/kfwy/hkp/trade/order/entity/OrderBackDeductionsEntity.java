package com.kfwy.hkp.trade.order.entity;


import com.gniuu.framework.entity.BaseEntity;

import java.math.BigDecimal;


/**
 *
 * @author lzp
 * @date 2019/06/28
 */
public class OrderBackDeductionsEntity extends BaseEntity {

    /**
     * 订单编号
     */
    protected String orderCode;
    /**
     * 已扣返佣总额
     */
    protected BigDecimal deductMoney;

    /**
     * 历史回款总额
     */
    protected BigDecimal  historyPayBackMoney;
    /**
     * 返佣总额
     */

    protected BigDecimal backMoney;
    /**
     * 历史开票扣款总额
     */
    protected BigDecimal historyInvoiceMoney;
    /**
     * 未扣开票总额
     */
    protected BigDecimal unInvoiceMoney;


    public String getOrderCode() {
        return orderCode;
    }

    public void setOrderCode(String orderCode) {
        this.orderCode = orderCode;
    }

    public BigDecimal getDeductMoney() {
        return deductMoney;
    }

    public void setDeductMoney(BigDecimal deductMoney) {
        this.deductMoney = deductMoney;
    }

    public BigDecimal getHistoryPayBackMoney() {
        return historyPayBackMoney;
    }

    public void setHistoryPayBackMoney(BigDecimal historyPayBackMoney) {
        this.historyPayBackMoney = historyPayBackMoney;
    }

    public BigDecimal getBackMoney() {
        return backMoney;
    }

    public void setBackMoney(BigDecimal backMoney) {
        this.backMoney = backMoney;
    }

    public BigDecimal getHistoryInvoiceMoney() {
        return historyInvoiceMoney;
    }

    public void setHistoryInvoiceMoney(BigDecimal historyInvoiceMoney) {
        this.historyInvoiceMoney = historyInvoiceMoney;
    }

    public BigDecimal getUnInvoiceMoney() {
        return unInvoiceMoney;
    }

    public void setUnInvoiceMoney(BigDecimal unInvoiceMoney) {
        this.unInvoiceMoney = unInvoiceMoney;
    }
}
