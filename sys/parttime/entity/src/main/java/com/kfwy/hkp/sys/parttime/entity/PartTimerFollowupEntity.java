package com.kfwy.hkp.sys.parttime.entity;


import com.gniuu.framework.entity.BaseEntity;

import java.util.Date;

/**
 * Created by kfwy_it_003 on 2017/7/8.
 */
public class PartTimerFollowupEntity extends BaseEntity {

    /**
     * 兼职用户编号
     */
    private String ptCode;
    /**
     * 跟进类型
     */
    private String followupType;
    /**
     * 备注
     */
    private String remark;
    /**
     * 创建人编码
     */
    private String createEmpCode;

    /**
     *最后修改人编码
     */
    private String updateEmpCode;
    /**
     * 创建部门编码
     */
    private String  createDeptCode;
    /**
     * 是否删除
     */
    private Boolean isDeleted;
    /**
     * 创建时间
     */
    private Date createTime;
    /**
     * 最后修改时间
     */
    private Date lastUpdateTime;

    public String getPtCode() {
        return ptCode;
    }

    public void setPtCode(String ptCode) {
        this.ptCode = ptCode;
    }

    public String getFollowupType() {
        return followupType;
    }

    public void setFollowupType(String followupType) {
        this.followupType = followupType;
    }

    @Override
    public String getRemark() {
        return remark;
    }

    @Override
    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getCreateEmpCode() {
        return createEmpCode;
    }

    public void setCreateEmpCode(String createEmpCode) {
        this.createEmpCode = createEmpCode;
    }

    public String getUpdateEmpCode() {
        return updateEmpCode;
    }

    public void setUpdateEmpCode(String updateEmpCode) {
        this.updateEmpCode = updateEmpCode;
    }

    @Override
    public String getCreateDeptCode() {
        return createDeptCode;
    }

    @Override
    public void setCreateDeptCode(String createDeptCode) {
        this.createDeptCode = createDeptCode;
    }

    public Boolean getDeleted() {
        return isDeleted;
    }

    public void setDeleted(Boolean deleted) {
        isDeleted = deleted;
    }

    @Override
    public Date getCreateTime() {
        return createTime;
    }

    @Override
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    @Override
    public Date getLastUpdateTime() {
        return lastUpdateTime;
    }

    @Override
    public void setLastUpdateTime(Date lastUpdateTime) {
        this.lastUpdateTime = lastUpdateTime;
    }
}
