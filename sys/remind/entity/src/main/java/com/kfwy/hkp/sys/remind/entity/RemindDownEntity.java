package com.kfwy.hkp.sys.remind.entity;

import com.gniuu.framework.entity.BaseEntity;

import java.util.Date;

/**
 * @Auther: HJH
 * @Date: 2019/4/19
 * @Description: TODO
 */
public class RemindDownEntity extends BaseEntity {

    // 提醒编码
    private String remindCode;
    // 下架关联code
    private String ownerCode;
    // 下架名称
    private String ownerName;
    // 下架类型
    private String downType;
    // 下架原因
    private String downCause;

    // 操作人编码
    private String userCode;
    // 操作原因
    private String userCause;
    // 是否操作
    private Boolean hasOperate;
    // 租赁时间
    private Date leaseExpiration;

    public String getRemindCode() {
        return remindCode;
    }

    public void setRemindCode(String remindCode) {
        this.remindCode = remindCode;
    }

    @Override
    public String getOwnerCode() {
        return ownerCode;
    }

    @Override
    public void setOwnerCode(String ownerCode) {
        this.ownerCode = ownerCode;
    }

    @Override
    public String getOwnerName() {
        return ownerName;
    }

    @Override
    public void setOwnerName(String ownerName) {
        this.ownerName = ownerName;
    }

    public String getDownType() {
        return downType;
    }

    public void setDownType(String downType) {
        this.downType = downType;
    }

    public String getDownCause() {
        return downCause;
    }

    public void setDownCause(String downCause) {
        this.downCause = downCause;
    }

    public String getUserCode() {
        return userCode;
    }

    public void setUserCode(String userCode) {
        this.userCode = userCode;
    }

    public String getUserCause() {
        return userCause;
    }

    public void setUserCause(String userCause) {
        this.userCause = userCause;
    }

    public Boolean getHasOperate() {
        return hasOperate;
    }

    public void setHasOperate(Boolean hasOperate) {
        this.hasOperate = hasOperate;
    }

    public Date getLeaseExpiration() {
        return leaseExpiration;
    }

    public void setLeaseExpiration(Date leaseExpiration) {
        this.leaseExpiration = leaseExpiration;
    }
}
