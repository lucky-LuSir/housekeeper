package com.kfwy.hkp.controller.file.vo;

import com.gniuu.framework.service.AbstractServiceRequest;
import com.kfwy.hkp.sys.file.entity.FileEntity;

import java.util.Date;

/**
 * @version 1.0
 * @description TODO
 * @auth davidCun
 * @date 2018/6/28 11:36
 */
public class FileServiceRequest extends AbstractServiceRequest<FileEntity> {

    private Date startTime;

    private Date endTime;

    private Integer width;

    private Integer height;

    private Integer size;

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public Integer getWidth() {
        return width;
    }

    public void setWidth(Integer width) {
        this.width = width;
    }

    public Integer getHeight() {
        return height;
    }

    public void setHeight(Integer height) {
        this.height = height;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }
}
