package com.kfwy.hkp.controller.sys.user.vo;

import com.gniuu.framework.service.AbstractServiceRequest;
import com.kfwy.hkp.sys.user.entity.UserEntity;

/**
 * Created by davidcun on 2018/5/18.
 */
public class AppUserServiceRequest extends AbstractServiceRequest<UserEntity> {
    /**
     * 验证码
     */
    private String verifyCode;

    /**
     * 手机解绑验证码

     */
    private String unBindVerifyCode;
    /**
     * 手机号
     */
    private String userPhone;
    /**
     * 密码
     */
    private String secure;

    private String deviceToken;
    /**
     * 设备绑定号
     */
    private String identifyNumber;
    /**
     * 设备名
     */
    private String deviceBrand;

    public String getDeviceBrand() {
        return deviceBrand;
    }

    public void setDeviceBrand(String deviceBrand) {
        this.deviceBrand = deviceBrand;
    }

    public String getIdentifyNumber() {
        return identifyNumber;
    }

    public void setIdentifyNumber(String identifyNumber) {
        this.identifyNumber = identifyNumber;
    }

    public String getSecure() {
        return secure;
    }

    public void setSecure(String secure) {
        this.secure = secure;
    }

    public String getUserPhone() {
        return userPhone;
    }

    public void setUserPhone(String userPhone) {
        this.userPhone = userPhone;
    }

    public String getVerifyCode() {
        return verifyCode;
    }

    public void setVerifyCode(String verifyCode) {
        this.verifyCode = verifyCode;
    }

    public String getDeviceToken() {
        return deviceToken;
    }

    public void setDeviceToken(String deviceToken) {
        this.deviceToken = deviceToken;
    }

    public String getUnBindVerifyCode() {
        return unBindVerifyCode;
    }

    public void setUnBindVerifyCode(String unBindVerifyCode) {
        this.unBindVerifyCode = unBindVerifyCode;
    }
}
