package com.kfwy.hkp.controller.sys.auth.vo;

import com.gniuu.framework.service.AbstractServiceRequest;
import com.kfwy.hkp.sys.auth.entity.RoleEntity;

import java.util.List;

/**
 * Created by davidcun on 2018/5/24.
 */
public class RoleServiceRequest extends AbstractServiceRequest<RoleEntity> {
    private List<String> menuCodes;
    private String roleCode;
    private String empPostCode;
    private String empPostName;

    public String getEmpPostName() {
        return empPostName;
    }

    public void setEmpPostName(String empPostName) {
        this.empPostName = empPostName;
    }

    public String getEmpPostCode() {
        return empPostCode;
    }

    public void setEmpPostCode(String empPostCode) {
        this.empPostCode = empPostCode;
    }

    public String getRoleCode() {
        return roleCode;
    }

    public void setRoleCode(String roleCode) {
        this.roleCode = roleCode;
    }

    public List<String> getMenuCodes() {
        return menuCodes;
    }

    public void setMenuCodes(List<String> menuCodes) {
        this.menuCodes = menuCodes;
    }
}
