public class OrderDivideRatioEntity extends BaseEntity {

    // 显示名称
    private String showName;
    // 显示数量
    private Integer showNum;
    // 订单数量
    private Integer orderNum;
    // 开单人数(去重)
    private Integer orderPersonNum;
    // 未开单人数
    private Integer notOrderPerson;
    // 开单比
    private String orderRatio;
    // 分成人数(去重)
    private Integer divideIntoPersonNum;
    // 分成比
    private String divideIntoRatio;

    public String getShowName() {
        return showName;
    }

    public void setShowName(String showName) {
        this.showName = showName;
    }

    public Integer getShowNum() {
        return showNum;
    }

    public void setShowNum(Integer showNum) {
        this.showNum = showNum;
    }

    public Integer getOrderNum() {
        return orderNum;
    }

    public void setOrderNum(Integer orderNum) {
        this.orderNum = orderNum;
    }

    public Integer getOrderPersonNum() {
        return orderPersonNum;
    }

    public void setOrderPersonNum(Integer orderPersonNum) {
        this.orderPersonNum = orderPersonNum;
    }

    public Integer getNotOrderPerson() {
        return notOrderPerson;
    }

    public void setNotOrderPerson(Integer notOrderPerson) {
        this.notOrderPerson = notOrderPerson;
    }

    public String getOrderRatio() {
        return orderRatio;
    }

    public void setOrderRatio(String orderRatio) {
        this.orderRatio = orderRatio;
    }

    public Integer getDivideIntoPersonNum() {
        return divideIntoPersonNum;
    }

    public void setDivideIntoPersonNum(Integer divideIntoPersonNum) {
        this.divideIntoPersonNum = divideIntoPersonNum;
    }

    public String getDivideIntoRatio() {
        return divideIntoRatio;
    }

    public void setDivideIntoRatio(String divideIntoRatio) {
        this.divideIntoRatio = divideIntoRatio;
    }
}
