package com.kfwy.hkp.controller.count.report.vo;

import com.gniuu.framework.service.AbstractServiceRequest;
import com.kfwy.hkp.bi.count.entity.ReportEntity;

import java.util.Date;

/**
 * Create By hjh on 2018/10/24
 */
public class ReportServiceRequest extends AbstractServiceRequest<ReportEntity> {

    // 编码
    private String code;
    // 开始时间
    private Date startDate;
    // 结束时间
    private Date endDate;
    // 是否可取缓存
    private boolean dynamic;
    //图表时间维度  月/天
    private String timeStr;
    //图表时间
    private Date time;

    private String province;

    private String city;

    private String region;

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getTimeStr() {
        return timeStr;
    }

    public void setTimeStr(String timeStr) {
        this.timeStr = timeStr;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    public Date getEndDate() {
        return endDate;
    }

    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }

    public boolean isDynamic() {
        return dynamic;
    }

    public boolean getDynamic() {
        return dynamic;
    }

    public void setDynamic(boolean dynamic) {
        this.dynamic = dynamic;
    }
}
