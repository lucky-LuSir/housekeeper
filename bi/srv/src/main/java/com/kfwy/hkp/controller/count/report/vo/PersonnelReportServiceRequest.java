package com.kfwy.hkp.controller.count.report.vo;

import com.gniuu.framework.service.AbstractServiceRequest;
import com.kfwy.hkp.bi.count.dto.PersonnelReportDto;

import java.util.Date;

/**
 * Create By liwenishan on 2018/11/24
 */
public class PersonnelReportServiceRequest extends AbstractServiceRequest<PersonnelReportDto> {

    private boolean dynamic;
    private Date startTime;
    private Date endTime;

    public boolean isDynamic() {
        return dynamic;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public boolean getDynamic() {
        return dynamic;
    }

    public void setDynamic(boolean dynamic) {
        this.dynamic = dynamic;
    }
}
